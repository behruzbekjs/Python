# Python math module examples
import math
print(math.e)
print(math.pi)

# methods
print(math.fabs(-5)) # absolute value
print(math.fabs(8))  # absolute value

square_root = math.sqrt(25)
print(square_root)

print(math.pow(2, 5))  
print(math.pow(11, 3))

# N!
print(math.factorial(5))  # 5! = 120

# Up yuqoriga rounding
print(math.ceil(4.3)) 
print(math.ceil(4.7)) 

# Down Pastga rounding
print(math.floor(4.7))
print(math.floor(4.3))

# Sin, Cos, Tan
print(math.sin(math.radians(30)))  # sin(30 degrees)
print(math.cos(math.radians(60)))  # cos(60 degrees)

